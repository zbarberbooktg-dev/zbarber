const STORAGE_KEY = "gbc-admin-lang";

type Ctx = { lang: Lang; setLang: (l: Lang) => void; t: (typeof translations)[Lang]; locale: string };
const I18nContext = createContext<Ctx | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("fr");
  useEffect(() => {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === "fr" || stored === "en") setLangState(stored);
  }, []);
  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem(STORAGE_KEY, l);
  };
  const value = useMemo<Ctx>(
    () => ({ lang, setLang, t: translations[lang], locale: lang === "fr" ? "fr-FR" : "en-US" }),
    [lang],
  );
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useT() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useT must be used within I18nProvider");
  return ctx;
}

export function LanguageToggle({ className }: { className?: string }) {
  const { lang, setLang } = useT();
  return (
    <div className={`inline-flex items-center gap-0.5 rounded-md border border-white/10 bg-white/5 p-0.5 ${className ?? ""}`}>
      {(["fr", "en"] as const).map((l) => {
        const active = lang === l;
        return (
          <button
            key={l}
            type="button"
            onClick={() => setLang(l)}
            className={`px-2 py-0.5 text-[10px] font-bold uppercase rounded transition-colors ${
              active ? "bg-white/20 text-white" : "text-white/60 hover:text-white"
            }`}
            aria-pressed={active}
          >
            {l}
          </button>
        );
      })}
    </div>
  );
}
